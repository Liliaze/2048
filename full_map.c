#include "game.h"

int		full_map(t_map *tab)
{
	int res;

	res = 0;
	tab->j = 0;
	while (tab->j++ < 3 && !(tab->i = 0))
		while (tab->i++ < 3)
			if (tab->map[tab->j][tab->i] == 0)
				res = 1;
	return (res);
}
